void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  /* Prevent unused argument(s) compilation warning */
  UNUSED(GPIO_Pin);

  uint32_t current_time = HAL_GetTick();	// Tiempo transcurrido de la última pulsación
  uint8_t index_btn;						// índice del botón preionado

  // Para identificar el botón presionado:
  if(GPIO_Pin == S1_Pin){
	  index_btn = 0;
	  index_pines = 0;
	  HAL_UART_Transmit(&huart2, "Se oprimió el botón A1\r\n",26,10);
  }else if(GPIO_Pin == S2_Pin){
	  index_btn = 1;
	  index_pines = 1;
	  HAL_UART_Transmit(&huart2, "Se oprimió el botón A2\r\n",26,10);
  }else{
	  HAL_UART_Transmit(&huart2, "NULL\r\n", 6,10);
	  return;
  }

  // Comprobamos el tiempo de la última pulsación:
  if(current_time - last_press_time[index_btn] > 250){
	  last_press_time[index_btn] = current_time; // Actualiza el tiempo

		  //Se encienden tres veces.
		  if(index_btn == 0){
			  cont_left = 6;
			  cont_right = 0; // Para parar el derecho cuando se presione el izquierdo
			  HAL_UART_Transmit(&huart2, "Se enciende el led IZQUIERDO\r\n", 30,10);
		  }else if(index_btn == 1){
			   cont_right = 6;
			   cont_left = 0; // Para parar el izquierdo cuando se presione el derecho
			  HAL_UART_Transmit(&huart2, "Se enciende el led DERECHO\r\n", 28,10);
		  }
	  }
}

// Configuración del hearbeat:
void heartbeat(void){
	static uint32_t tick = 0;
	if(tick < HAL_GetTick()){
		tick = HAL_GetTick() + 1000;
		HAL_GPIO_TogglePin(D4_GPIO_Port, D4_Pin);
	}
}

// Configuración del
void led_left(void){
	static uint32_t left_tick = 0;
	if(left_tick < HAL_GetTick()){
		if(cont_left > 0){
			left_tick =  HAL_GetTick() + 250;
			HAL_GPIO_TogglePin(D1_GPIO_Port, D1_Pin);
			cont_left--;
		}else{
		HAL_GPIO_WritePin(D1_GPIO_Port, D1_Pin, 1);
		}
	}
}

void led_right(void){
	static uint32_t right_tick = 0;
	if(right_tick < HAL_GetTick()){
		if(cont_right > 0){
			right_tick =  HAL_GetTick() + 250;
			HAL_GPIO_TogglePin(D2_GPIO_Port, D2_Pin);
			cont_right--;
		}else{
		HAL_GPIO_WritePin(D2_GPIO_Port, D2_Pin, 1);
		}
	}
}

